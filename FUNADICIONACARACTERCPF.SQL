CREATE OR REPLACE FUNCTION FUNADICIONACARACTERCPF(PCPF VARCHAR2) RETURN VARCHAR2 IS
   RESULT         VARCHAR2(20);
   S_CPF          TBUSUARIO.CPF%TYPE;
   S_STRING_FINAL VARCHAR(20);
BEGIN
   SELECT SUBSTR(PCPF, 1, 3)
   INTO S_CPF
   FROM DUAL;

   S_STRING_FINAL := S_CPF || '.';

   SELECT SUBSTR(PCPF, 4, 3)
   INTO S_CPF
   FROM DUAL;

   S_STRING_FINAL := S_STRING_FINAL || S_CPF || '.';

   SELECT SUBSTR(PCPF, 7, 3)
   INTO S_CPF
   FROM DUAL;

   S_STRING_FINAL := S_STRING_FINAL || S_CPF;

   SELECT SUBSTR(PCPF, 10, 3)
   INTO S_CPF
   FROM DUAL;



   S_STRING_FINAL := S_STRING_FINAL || '-' || S_CPF;


   RESULT := S_STRING_FINAL;
   RETURN(RESULT);
END FUNADICIONACARACTERCPF;
/
