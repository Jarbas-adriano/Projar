CREATE OR REPLACE TRIGGER TGHEADER_ORCAMENTO
BEFORE INSERT OR UPDATE OR DELETE ON TBHEADER_ORCAMENTO FOR EACH ROW
DECLARE
   SRAZAO_SOCIAL TBCLIENTE.NOME_NOME_FANTASIA%TYPE;
BEGIN
   IF INSERTING THEN
      SELECT A.NOME_NOME_FANTASIA
      INTO SRAZAO_SOCIAL
      FROM TBCLIENTE A
      WHERE A.COD_EMPRESA = :NEW.COD_EMPRESA AND
            A.COD_CLIENTE = :NEW.COD_CLIENTE;
   
      :NEW.NOME_NOME_FANTASIA_CLIENTE := SRAZAO_SOCIAL;
   END IF;
END;
/
